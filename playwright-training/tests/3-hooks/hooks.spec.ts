//Hooks in Playwright : Hooks are nothing but a set of conditions that we want to run before and after the test execution.

// There are 4 different hooks in Playwright :

//1. beforeAll : This will run one time before all the test cases within the described block.
//2. afterAll : This will run one time after all the test cases are executed within the described block.
//3. beforeEach : This will run before each and every test case.
//4. afterEach : This will run after each and every test case.

